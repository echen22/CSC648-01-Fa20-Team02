package main.server.PatientInjury;

public class PatientInjury {

    private Integer patientInjuryId;
    private Integer patient;
    private Integer injury;


    public Integer getPatientInjuryId() {
        return patientInjuryId;
    }

    public void setPatientInjuryId(Integer patientInjuryId) {
        this.patientInjuryId = patientInjuryId;
    }


    public Integer getPatient() {
        return patient;
    }

    public void setPatient(Integer patient) {
        this.patient = patient;
    }


    public Integer getInjury() {
        return injury;
    }

    public void setInjury(Integer injury) {
        this.injury = injury;
    }

}

